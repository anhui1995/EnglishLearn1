package xin.xiaoa.englishlearn.click_word;

public class WordItem {
    private String english;
    private String fayin;
    private String yinbiao;
    private String n;
    private String v;
    private String adj;
    private String adv;
    private String other;
    private int id;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getEnglish() {
        return english;
    }

    public void setEnglish(String english) {
        this.english = english;
    }

    public String getFayin() {
        return fayin;
    }

    public void setFayin(String fayin) {
        this.fayin = fayin;
    }

    public String getYinbiao() {
        return yinbiao;
    }

    public void setYinbiao(String yinbiao) {
        this.yinbiao = yinbiao;
    }

    public String getN() {
        return n;
    }

    public void setN(String n) {
        this.n = n;
    }

    public String getV() {
        return v;
    }

    public void setV(String v) {
        this.v = v;
    }

    public String getAdj() {
        return adj;
    }

    public void setAdj(String adj) {
        this.adj = adj;
    }

    public String getAdv() {
        return adv;
    }

    public void setAdv(String adv) {
        this.adv = adv;
    }

    public String getOther() {
        return other;
    }

    public void setOther(String other) {
        this.other = other;
    }




}
